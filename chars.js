function padChar(char, left, right) {
  for (var i = 0; i < 8 ;i++) {
    if (left === true) {
      char[i].unshift(0);
    }

    if (right === true) {
      char[i].push(0);
    }
  }

  return char;
}

module.exports.getChar = function(char, monospace) {
  if (chars.hasOwnProperty(char) == -1) {
    throw new Error('Unknow char: ' + char);
  }

  if (monospace === true) {
    var charBuffer = chars[char];
    if (charBuffer.length < 8) {
      if (charBuffer.length%2 !== 0) {
        charBuffer = padChar(charBuffer, true, false);
      }

      while (charBuffer.length < 8) {
        charBuffer = padChar(charBuffer, true, true);
      }
    }

    return charBuffer;
  }

  return chars[char];
}
